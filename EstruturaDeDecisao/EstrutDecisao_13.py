dia = int(input("De 1 à 7, informe o número e lhe direi o dia da semana."))

if dia == 1:
    print(f"{dia} - Domingo")
elif dia == 2:
    print(f"{dia} - Segunda")
elif dia == 3:
    print(f"{dia} - Terça")
elif dia == 4:
    print(f"{dia} - Quarta")
elif dia == 5:
    print(f"{dia} - Quinta")
elif dia == 6:
    print(f"{dia} - Sexta")
elif dia == 7:
    print(f"{dia} - Sábado")
else:
    print("Valor inválido.")